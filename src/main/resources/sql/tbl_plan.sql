CREATE SEQUENCE SEQ_PLAN;
CREATE TABLE TBL_PLAN (
                          ID NUMBER CONSTRAINT PK_PLAN PRIMARY KEY,
                          PLAN_NAME VARCHAR2(1000) NOT NULL,
                          PLAN_START_DATE DATE DEFAULT SYSDATE,
                          PLAN_END_DATE DATE DEFAULT SYSDATE,
                          PLAN_MAX_PERSONNEL NUMBER NOT NULL,
                          PLAN_MIN_PERSONNEL NUMBER NOT NULL,
                          PLAN_PRICE NUMBER NOT NULL,
                          PLAN_GATHERING_ADDR VARCHAR2(2000) NOT NULL,
                          PLAN_INTRODUCTION VARCHAR2(2000) NOT NULL,
                          PLAN_FILE_PATH VARCHAR2(2000) NOT NULL,
                          PLAN_FILE_NAME VARCHAR2(2000) NOT NULL,
                          PLAN_REGIST_DATE DATE DEFAULT NULL,
                          PLAN_REGIST_CANCEL_DATE DATE DEFAULT NULL,
                          MEMBER_ID NUMBER NOT NULL,
                          COURSE_ID NUMBER NOT NULL,
                          CREATE_DATE DATE DEFAULT SYSDATE,
                          UPDATE_DATE DATE DEFAULT SYSDATE,
                          CONSTRAINT FK_PLAN_MEMBER FOREIGN KEY(MEMBER_ID) REFERENCES TBL_MEMBER(ID),
                          CONSTRAINT FK_PLAN_COURSE FOREIGN KEY(COURSE_ID) REFERENCES TBL_COURSE(ID)
);

COMMENT ON TABLE TBL_PLAN IS '계획 테이블';
COMMENT ON COLUMN TBL_PLAN.ID IS '계획의 고유 ID';
COMMENT ON COLUMN TBL_PLAN.PLAN_NAME IS '계획 이름';
COMMENT ON COLUMN TBL_PLAN.PLAN_START_DATE IS '계획 시작 날짜';
COMMENT ON COLUMN TBL_PLAN.PLAN_END_DATE IS '계획 종료 날짜';
COMMENT ON COLUMN TBL_PLAN.PLAN_MAX_PERSONNEL IS '계획 최대 인원';
COMMENT ON COLUMN TBL_PLAN.PLAN_MIN_PERSONNEL IS '계획 최소 인원';
COMMENT ON COLUMN TBL_PLAN.PLAN_PRICE IS '계획 가격';
COMMENT ON COLUMN TBL_PLAN.PLAN_GATHERING_ADDR IS '계획 모임 주소';
COMMENT ON COLUMN TBL_PLAN.PLAN_INTRODUCTION IS '계획 소개';
COMMENT ON COLUMN TBL_PLAN.PLAN_FILE_PATH IS '계획 파일 경로';
COMMENT ON COLUMN TBL_PLAN.PLAN_FILE_NAME IS '계획 파일 이름';
COMMENT ON COLUMN TBL_PLAN.PLAN_REGIST_DATE IS '계획 등록 날짜';
COMMENT ON COLUMN TBL_PLAN.PLAN_REGIST_CANCEL_DATE IS '계획 등록 취소 날짜';
COMMENT ON COLUMN TBL_PLAN.MEMBER_ID IS '멤버의 고유 ID';
COMMENT ON COLUMN TBL_PLAN.COURSE_ID IS '코스의 고유 ID';
COMMENT ON COLUMN TBL_PLAN.CREATE_DATE IS '생성 날짜';
COMMENT ON COLUMN TBL_PLAN.UPDATE_DATE IS '수정 날짜';